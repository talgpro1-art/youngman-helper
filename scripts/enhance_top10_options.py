def main():
    if not OPTION_FILE.exists():
        raise FileNotFoundError(f"option_summary.csv not found: {OPTION_FILE}")

    df = pd.read_csv(OPTION_FILE)
    df.to_csv(BACKUP_FILE, index=False, encoding="utf-8-sig")

    cols = ["rank", "brand", "model", "option_name", "function", "sales_point", "data_status"]
    enhanced = pd.DataFrame(enhanced_rows, columns=cols)

    # TOP10 기존 행 제거 후 고도화 행 삽입
    top10_keys = set((r[1], r[2]) for r in enhanced_rows)
    keep = df[~df.apply(lambda x: (str(x.get("brand")), str(x.get("model"))) in top10_keys, axis=1)].copy()

    result = pd.concat([enhanced, keep], ignore_index=True)
    result["rank"] = pd.to_numeric(result["rank"], errors="coerce").fillna(999).astype(int)
    result = result.sort_values(["rank", "brand", "model", "option_name"]).reset_index(drop=True)
    result.to_csv(OPTION_FILE, index=False, encoding="utf-8-sig")

    print(f"Updated: {OPTION_FILE}")
    print(f"Backup: {BACKUP_FILE}")
    print(f"Enhanced TOP10 option rows: {len(enhanced)}")
    print(f"Total rows: {len(result)}")

if __name__ == "__main__":
    main()
